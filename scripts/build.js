function splitByH2(markdown) {
  const parts = markdown.split(/\n---\n/);
  return parts.map((p) => p.trim()).filter(Boolean);
}

function buildSections() {
  const result = [];

  for (const def of SECTION_MAP) {
    const filePath = join(CONTENT_DIR, def.file);
    const raw = readFileSync(filePath, 'utf-8');

    if (def.sections) {
      // Multi-section file: split by ---
      const parts = splitByH2(raw);
      for (let i = 0; i < def.sections.length; i++) {
        const sec = def.sections[i];
        const md = parts[i] || '';
        const html = marked.parse(md);
        result.push({
          group: def.group,
          id: sec.id,
          title: sec.title,
          html: `\n${html.trim()}\n`,
        });
      }
    } else {
      // Single section file
      const html = marked.parse(raw);
      result.push({
        group: def.group,
        id: def.id,
        title: def.title,
        html: `\n${html.trim()}\n`,
      });
    }
  }

  return result;
}

const sections = buildSections();

// Use JSON then convert to single-quote JS for biome compatibility
const json = JSON.stringify(sections, null, 2);
const js = json
  .replace(/"([^"]+)":/g, '$1:')
  .replace(/"/g, "'")
  .replace(/\\'/g, "\\'");

const output = `// Auto-generated by scripts/build.js — DO NOT EDIT
// Run: npm run build
const sections = ${js};
`;

writeFileSync(OUTPUT, output, 'utf-8');
console.log(`✓ Built ${sections.length} sections → js/data.js`);
